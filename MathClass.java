public class MathClass {

    public static void main(String[] args) {
        double w, x, y, z, a, b, c;
        x = 3.14;
        y = -10; 
        z = Math.min(x, y);
        w = Math.max(x, y);
        a = Math.abs(y);
        b = Math.sqrt(x);
        c = Math.round(b);
        
        System.out.println(z);
        System.out.println(a);
        System.out.println(b);
        System.out.println(w);
        System.out.println(c);
        
    }
    
}
